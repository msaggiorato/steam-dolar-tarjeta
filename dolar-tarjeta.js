
var xhr = new XMLHttpRequest();
xhr.onload = function(data) {
	var dolar = $.parseJSON(substr(xhr.responseText, 19, -2));
	var dolarTarjeta = parseFloat((parseFloat(dolar["CasaCambioVentaValue"].replace(",",".")) * 1.35).toFixed(3));
	var timeout;
	var convertToArsClean = function(price) {
		var oldPrice = parseFloat(price.replace(" USD", "").replace("$", ""));
		var newPrice = parseFloat((oldPrice * dolarTarjeta).toFixed(2));
		return newPrice;
	}
	var convertToArs = function(price, symbol) {
		if(typeof symbol == "undefined")
			symbol = ""
		else
			symbol = " "+symbol;

		var newPrice = convertToArsClean(price);
		if(isNaN(newPrice))
			return price;

		return "$"+newPrice.toFixed(2)+symbol;
	}
	var check = function(){
		$(".discount_final_price").not(".tarjetized").each(function(){
			$(this).html(convertToArs($(this).html(), "ARS")).addClass('tarjetized');
		});
		$(".discount_original_price").not(".tarjetized").each(function(){
			$(this).html(convertToArs($(this).html())).addClass('tarjetized');
		});

		$(".game_purchase_action_bg .game_purchase_price,"+
			" #search_suggestion_contents .match_price,"+
			" #header_wallet_balance,"+
			" .transactionRowPrice,"+
			" .cart_item_price .price,"+
			" #cart_price_total").not(".tarjetized").each(function(){
			$(this).html(convertToArs($(this).html(), "ARS")).addClass('tarjetized');
		})

		$(".col.search_price").not(".tarjetized").each(function(){
			var wholeText = trim($(this).text());
			var fullPrice = trim($(this).find("span").text());
			var finalPrice = wholeText.replace(fullPrice, "");
			var newHTML = "";
			if(fullPrice != ""){
				newHTML += '<span style="color: #888888;"><strike>'+"$"+convertToArsClean(fullPrice).toFixed(2)+'</strike></span><br>';
			}
			var newPrice = convertToArsClean(finalPrice);
			newHTML += "$"+newPrice.toFixed(2)+" ARS";
			if(isNaN(newPrice)){
				$(this).addClass('tarjetized');
				return;
			}
			$(this).html(newHTML)
			$(this).addClass('tarjetized');
		});
		clearTimeout(timeout);
		timeout = setTimeout(check, 500);
	}
	check();

}
